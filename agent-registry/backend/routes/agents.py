LIFECYCLE_ORDER = ["dev", "staging", "prod", "deprecated"]
PROTECTION_ORDER = ["unprotected", "awaiting_event", "protected"]

# Hash-named / autogenerated trace artefacts that aren't real agents.
_HEX32 = re.compile(r"^[0-9a-f]{32}$", re.I)
_KICKOFF = re.compile(r"kickoff$", re.I)


def _is_trace_junk(agent: Agent) -> bool:
    """A trace-id-named record (e.g. '69590eae...' or 'Crew_xxx.kickoff'), not a real agent."""
    name = (agent.name or "").strip()
    slug = (agent.slug or "").strip()
    if _HEX32.match(name) or _HEX32.match(slug):
        return True
    if _KICKOFF.search(slug) or _KICKOFF.search(name):
        return True
    return False


def _dup_score(agent: Agent) -> tuple:
    """Higher tuple = better record to keep when de-duplicating a slug group."""
    prot = PROTECTION_ORDER.index(agent.protection_status) if agent.protection_status in PROTECTION_ORDER else 0
    life = LIFECYCLE_ORDER.index(agent.lifecycle) if agent.lifecycle in LIFECYCLE_ORDER else 0
    deps = agent.dependencies or {}
    dep_count = sum(len(deps.get(k, []) or []) for k in ("models", "tools", "dataSources", "agents"))
    threat_count = len(agent.threats or [])
    audit_count = len(agent.audit_log or [])
    has_owner = 1 if agent.owner else 0
    # created_at as a tiebreaker: keep the oldest (negative timestamp sorts later=smaller)
    created = -(agent.created_at.timestamp() if agent.created_at else 0)
    return (prot, life, has_owner, dep_count, threat_count, audit_count, created)


